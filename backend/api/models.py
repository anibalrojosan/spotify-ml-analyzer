import uuid
from django.db import models

class Track(models.Model):
    """
    Represents a unique song.
    Acts as the central entity for descriptive metadata.
    """
    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    spotify_id = models.CharField(max_length=255, unique=True, db_index=True)
    
    title = models.CharField(max_length=255)
    artist_name = models.CharField(max_length=255)
    album_name = models.CharField(max_length=255)
    
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    def __str__(self):
        return f"{self.title} - {self.artist_name}"

class AudioFeatures(models.Model):
    """
    Contains raw numerical audio data (0.0 - 1.0) for ML.
    1:1 relationship with Track.
    """
    track = models.OneToOneField(Track, on_delete=models.CASCADE, related_name='features')
    
    danceability = models.FloatField()
    energy = models.FloatField()
    valence = models.FloatField()
    acousticness = models.FloatField()
    tempo = models.FloatField()
    
    # Additional useful fields from dataset/API with default values
    instrumentalness = models.FloatField(default=0.0)
    liveness = models.FloatField(default=0.0)
    loudness = models.FloatField(default=0.0)
    speechiness = models.FloatField(default=0.0)
    
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    def __str__(self):
        return f"Features for {self.track.title}"

class UserArchetype(models.Model):
    """
    Defines pre-calculated profiles (Clusters) for simulation.
    """
    name = models.SlugField(unique=True) # e.g., 'the-sad-rocker'
    display_name = models.CharField(max_length=100)
    description = models.TextField()
    
    # Value ranges to filter tracks matching this archetype
    min_values = models.JSONField(help_text="Dictionary of minimum values (e.g., {'energy': 0.8})")
    max_values = models.JSONField(help_text="Dictionary of maximum values")
    
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    def __str__(self):
        return self.display_name

class UserProfile(models.Model):
    """
    Represents the end user (simulated or real).
    Links a user ID with their history and archetype.
    """
    spotify_id = models.CharField(max_length=255, unique=True, db_index=True)
    display_name = models.CharField(max_length=255)
    
    # Archetype assigned in simulation (can be null if it's a new real user)
    archetype = models.ForeignKey(UserArchetype, on_delete=models.SET_NULL, null=True, blank=True)
    
    # Listening history (Simulated or Real)
    tracks = models.ManyToManyField(Track, related_name='listeners', blank=True)
    
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    def __str__(self):
        return self.display_name

class UserInsight(models.Model):
    """
    Stores reports generated by AI (Gemini).
    """
    user = models.ForeignKey(UserProfile, on_delete=models.CASCADE, related_name='insights')
    personality_label = models.CharField(max_length=255)
    llm_response = models.TextField()
    cluster_centers = models.JSONField(help_text="Centroid data for charts")
    
    created_at = models.DateTimeField(auto_now_add=True)

    def __str__(self):
        return f"Insight for {self.user.display_name} ({self.created_at.date()})"
